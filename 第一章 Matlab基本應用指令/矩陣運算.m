%*矩陣運算Ex.
%     
% A = | 1 2 | B = | 5 6 |
%     | 3 4 |     | 7 8 |
%
%指令：
A=[1 2;3 4]
B=[5 6;7 8]

%ans= 
%    A = 
%        1	2
%        3	4
%    B =
%        5	6
%        7	8

%*矩陣相乘
%指令： 
A*B
%ans =
%   19	22
%   43	50

%*矩陣次方
%指令：
A^5 
%ans =
%   1069	1558
%   2337	3406

%*反矩陣指令：
inv(A)或A^(-1)
%ans =
%   -2.0000	1.0000
%   1.5000	-0.5000

%*行列式
%指令 ： 
det(A)
%ans =
%-2

%*矩陣轉置指令：
A'
%ans =
%   1	3
%   2	4
